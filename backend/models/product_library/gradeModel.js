const mongoose = require('mongoose');

const gradeSchema = new mongoose.Schema({
  grade_name: {
    type: String,
    required: true,
    unique: true,
  },
  description: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    enum: ['active', 'inactive'],
    default: 'active'
  }
}, {
  timestamps: true
});

module.exports = mongoose.model('Grade', gradeSchema);
