const mongoose = require("mongoose");

const itemSpecificationSchema = new mongoose.Schema(
  {
    item_specification_name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      enum: ["active", "inactive"],
      default: "active",
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("ItemSpecification", itemSpecificationSchema);
